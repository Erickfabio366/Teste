package estruturas;

public class Pessoa {
	String nome;
	String cpf;
	String rg;
	String dataNasc;
	String endereco;
	String telefone;
	String email;
	char sexo;
	
	public Pessoa(String nome, String cpf, String rg, String dataNasc, String endereco, String telefone, String email, char sexo) {
		this.nome = nome;
		this.cpf = cpf;
		this.rg = rg;
		this.dataNasc = dataNasc;
		this.endereco = endereco;
		this.telefone = telefone;
		this.email = email;
		this.sexo = sexo;
	}
	
	
}
